# operadores de comparações retornam tipo de dado booleano, verdadeiro ou falso
# == igual
# != diferente
# > maior
# >= maior ou igual
# < menor
# <= menor ou igual

saldo = 200
saque = 200
print(saldo == saque)
print(saldo != saque)
print(saldo > saque)
print(saldo >= saque)
print(saldo < saque)
print(saldo <= saque)