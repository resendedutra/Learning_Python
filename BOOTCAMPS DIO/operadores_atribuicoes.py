# = sinal de atibuição simples
# += sinal de atribuição com adição
# -= sinal de atribuição com subtração
# *= sinal de atribuição com multiplicação
# /= sinal de atribuição com divisão
# //= sinal de atribuição com divisão inteira
# %= sinal de atribuição com módulo ou resto da divisão
# **= sinal de atribuição com exponencial

saldo = 500
print(saldo)

saldo = 200
print(saldo)

saldo += 10
print(saldo)

saldo -= 5
print(saldo)

saldo //= 2
print(saldo)

saldo /= 2
print(saldo)

saldo *= 10
print(saldo)

saldo %= 4
print(saldo)

saldo **= 2
print(saldo)